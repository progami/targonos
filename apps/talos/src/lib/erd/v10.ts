export type ErdV10Entity = keyof typeof ERD_V10_ENTITY_COLUMNS

export const ERD_V10_ENTITIES: ErdV10Entity[] = Object.keys(ERD_V10_ENTITY_COLUMNS) as ErdV10Entity[]

export function isErdV10Entity(value: string): value is ErdV10Entity {
  return ERD_V10_ENTITIES.includes(value as ErdV10Entity)
}

export function parseErdV10Limit(raw: string | null, defaultLimit: number = 200): number {
  const candidate = raw === null ? defaultLimit : Number.parseInt(raw, 10)
  if (!Number.isFinite(candidate)) {
    return defaultLimit
  }
  if (candidate < 1) {
    return 1
  }
  if (candidate > 2000) {
    return 2000
  }
  return candidate
}
